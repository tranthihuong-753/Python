""" 1. Tính tổng, hiệu, tích, thương của hai số a và b nhập từ bàn phím và
in lên màn hình kết quả của chúng. """

a = float(input("Nhap vao so thu nhat : "))
b = float(input("Nhap vao so thu hai :"))

print(f"{a} + {b} = {a+b:.1f}")
print(f"{a} - {b} = {a-b:.1f}")
print(f"{a} * {b} = {a*b:.1f}")
print(f"{a} / {b} = {a/b:.1f}")